package Entities;

public class Player {

    private float posX;
    private float posY;

    public Player() {
        this.posX = 0;
        this.posY = 0;
    }

    public float getPosX() {
        return posX;
    }

    public void setPosX(float posX) {
        this.posX = posX;
    }

    public float getPosY() {
        return posY;
    }

    public void setPosY(float posY) {
        this.posY = posY;
    }
}
